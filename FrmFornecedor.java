    private void jTableFornecedorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableFornecedorMouseClicked
        // TODO add your handling code here:

        String nome_fornecedor = "" +jTableFornecedor.getValueAt(jTableFornecedor.getSelectedRow(),1);  
//            
        conn.conexao();
        conexaoPesquisa.conexao();
        conn.executaSQL("select * from fornecedores where nome_fornecedor='"+nome_fornecedor+"'");
        
        try {           
            conn.rs.first();
            jTextFieldCod.setText(String.valueOf(conn.rs.getInt("id_fornecedor")));
            jTextFieldNomeForn.setText(conn.rs.getString("nome_fornecedor"));
            jTextFieldEnd.setText(String.valueOf(conn.rs.getString("endereco")));
            
            conexaoPesquisa.executaSQL("select * from bairro where id_bairro=" +conn.rs.getInt("id_bairro"));          
            conexaoPesquisa.rs.first();
            jComboBoxBairro.setSelectedItem(conexaoPesquisa.rs.getString("nome_bairro"));
            conexaoPesquisa.desconecta();
            conn.desconecta();
            jTextFieldEnd.setEnabled(true);
            jTextFieldNomeForn.setEnabled(true);
            jFormattedTextFieldCNPJ.setEnabled(true);
            jButtonSalvar.setEnabled(true);
            jButtonNovo.setEnabled(false);
            jButtonEditar.setEnabled(true);
            jButtonExcluir.setEnabled(true);               
        } catch (SQLException s) {
            
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher ao selecionar os dados! n ERRO:"+s);
            conexaoPesquisa.desconecta();
            conn.desconecta();
        }
        
        
    }//GEN-LAST:event_jTableFornecedorMouseClicked

