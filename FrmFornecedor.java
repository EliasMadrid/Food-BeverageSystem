    public void preencherTabela(String SQL){
       ArrayList dados = new ArrayList();
       
       String [] Colunas = new String[] {"ID", "Nome", "Endereco"};
               conn.conexao();
               conn.executaSQL(SQL);
               try{
                  conn.rs.first();
                  do{
                      dados.add(new Object[]{conn.rs.getInt("id_fornecedor"), conn.rs.getString("nome_fornecedor"), conn.rs.getString("endereco")});
              }while(conn.rs.next());       
                 }catch (SQLException ex){
                     JOptionPane.showMessageDialog(rootPane, "Erro ao preencher o Array list! n ERRO:"+ex);
                   }  
               ModeloTabela modelo = new ModeloTabela(dados, Colunas);
               jTableFornecedor.setModel(modelo);
               jTableFornecedor.getColumnModel ().getColumn(0).setPreferredWidth(60);
               jTableFornecedor.getColumnModel().getColumn(0).setResizable(false);
               jTableFornecedor.getColumnModel ().getColumn(1).setPreferredWidth(240);
               jTableFornecedor.getColumnModel().getColumn(1).setResizable(false);
               jTableFornecedor.getColumnModel ().getColumn(2).setPreferredWidth(255);
               jTableFornecedor.getColumnModel().getColumn(2).setResizable(false);
               jTableFornecedor.getTableHeader().setReorderingAllowed(false);
               jTableFornecedor.setAutoResizeMode(jTableFornecedor.AUTO_RESIZE_OFF);
               jTableFornecedor.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
               conn.desconecta();
               
                
       }
    public void preencherCombobox(){
        conn.conexao();
        conn.executaSQL("select * from bairro order by nome_bairro");
        jComboBoxBairro.removeAllItems();
        try {
            conn.rs.first();
            do{
               jComboBoxBairro.addItem(""+conn.rs.getString("nome_bairro")); 
            }while(conn.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane,"Erro ao preencher os bairros!"+ ex);
        }
        conn.desconecta();
    }
