  public void preencherTabela(String SQL){
       ArrayList dados = new ArrayList();
       
       String [] Colunas = new String[] {"Cod_Parcela", "Cod_venda", "Valor","Estado"};
               conn.executaSQL(SQL);
               try{
                  conn.rs.first();
                  do{
                      dados.add(new Object[]{conn.rs.getInt("id_parc_venda"), conn.rs.getString("cod_venda"), conn.rs.getString("valor_parce"), conn.rs.getString("estado")});
              }while(conn.rs.next());       
                 }catch (SQLException ex){
                     JOptionPane.showMessageDialog(rootPane, "Erro ao preencher o Array list! n ERRO:"+ex);
                   }  
               ModeloTabela modelo = new ModeloTabela(dados, Colunas);
               tabelabaixa.setModel(modelo);
               tabelabaixa.getColumnModel ().getColumn(0).setPreferredWidth(110);
               tabelabaixa.getColumnModel().getColumn(0).setResizable(false);
               tabelabaixa.getColumnModel ().getColumn(1).setPreferredWidth(195);
               tabelabaixa.getColumnModel().getColumn(1).setResizable(false);
               tabelabaixa.getColumnModel ().getColumn(2).setPreferredWidth(115);
               tabelabaixa.getColumnModel().getColumn(2).setResizable(false);
               tabelabaixa.getColumnModel ().getColumn(3).setPreferredWidth(100);
               tabelabaixa.getColumnModel().getColumn(3).setResizable(false);
               tabelabaixa.getTableHeader().setReorderingAllowed(false);
               tabelabaixa.setAutoResizeMode(tabelabaixa.AUTO_RESIZE_OFF);
               tabelabaixa.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
                
       }
